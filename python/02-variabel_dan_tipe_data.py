# Variabel Dan Type Data

print(f'''
=======================================
Contoh Penulisan Variabel Dan Type Data
=======================================
''')

name = "Alice"  # Variabel name Bertipe data String
age = "25"      # Variabel age Bertipe data Integer
height = 1.68   # Variabel height Bertipe data Float (desimal)
is_happy = True # Variabel is_happy Bertipe data Boolean

print(f'''
=======================================
Pengecekan Type Variabel
=======================================
''')

print(f'''
{name} Bertipe data {type(name)}
{age} Bertipe data {type(age)}
{height} Bertipe data {type(height)}
{is_happy} Bertipe data {type(is_happy)}
''')
